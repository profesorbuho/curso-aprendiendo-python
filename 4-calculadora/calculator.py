import math

number_1 = 24
number_2 = 42

# Suma
suma = number_1 + number_2
print( suma )

# Resta
resta = number_2 - number_1
print( resta )

# Multiplicación
producto = number_1 * 2
print( producto )

# División
division = 48 / number_1
print( division )

# División entera
div_int = 5 // 2
print( div_int )

# Resto
resto = 5 % 2
print( resto )

# Potenciación
potencia = 2 ** 3
print( potencia )

operacion = (5 + 6) / 3 + (4 / 9)
print( operacion )

# Módulo math
print( math.ceil(5.24) )
print( math.floor(5.24) )
print( math.fabs(-52.4) )
print( math.pow(2, 3) )
print( math.sqrt(64) )
print( math.pi )
print( math.sin(math.pi / 3) )
print( math.asin(0.866) )
round_number = round(math.asin(0.866), 3)
print( round_number )